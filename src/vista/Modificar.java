    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String idPelicula;
        String tituloP;
        String descripcionP;
        String lanzamientoP;
        String duracionRenta;
        String costoP;

        
        idPelicula = IdM.getText();
        tituloP = NombreM.getText();
        descripcionP = DescripcionM.getText();
        lanzamientoP = LanzamientoM.getText();
        
        duracionRenta = DuracionRentaM.getText();
        costoP = CostoM.getText();
  
        peliculaModificar = new Pelicula(idPelicula, tituloP, descripcionP, lanzamientoP, duracionRenta, costoP);
        
        ControlPelicula  CP = new ControlPelicula();
        
        boolean t = CP.ModificarPelicula(idPelicula,peliculaModificar);
        
        if (t) {

                JOptionPane.showMessageDialog(null, "Datos de la pelicula guardados.");

            } else {

                JOptionPane.showMessageDialog(null, "Datos de la pelicula no guardados.");

            }
        
    }//GEN-LAST:event_jButton1ActionPerformed

